package com.digital.backend.bean.user;

public class BackendUserBean {
	int user_id;
	String user_email;
	String user_name;
	String user_password;
	String user_create_date;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public String getUser_create_date() {
		return user_create_date;
	}
	public void setUser_create_date(String user_create_date) {
		this.user_create_date = user_create_date;
	}
}
